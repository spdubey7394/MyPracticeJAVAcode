package Day3;

public class decrementoperator {

	public static void main(String[] args) {
		
		// case1 pre
		int b=10;
		b=b-1;
	//	b--;
		System.out.println(b);
		
		// case2 pre
		int be=10;
		int res=--be;
		System.out.println(res);
		System.out.println(be);
		
		// case 3 post
		
		int bee=10;
		bee--;
		
	System.out.println(bee);
		

		
		

	}

}
