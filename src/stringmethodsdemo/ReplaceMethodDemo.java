package stringmethodsdemo;

public class ReplaceMethodDemo {

	public static void main(String[] args) {
		String s="automation consist of framework, out of which one selenium automation";
		
		// replace method is used to replace single or multiple character n a string
		
		System.out.println(s.replace('a','Z')); // for single character single quotes
		
		System.out.println(s.replace("automation","Manual")); // for multiple character double quotes is used 

	}

}
